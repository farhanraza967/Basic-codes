#include<iostream> 
#include<cmath>   
using namespace std;   
int main() { 
const double pi = 3.141592; 
double angle = pi/6;  
cout<<endl<<"******** Calculating Trigonometric Ratios ********"<<endl; 
cout<<endl<<"All calculations on Angle "<<angle<<"Radians"<<endl; 
cout<<endl<<"cos("<<angle<<") "<<"= "<<cos(angle)<<endl;
cout<<endl<<"sin("<<angle<<") "<<"= "<<sin(angle)<<endl; 
cout<<endl<<"tan("<<angle<<") "<<"= "<<tan(angle)<<endl; 
cout<<endl<<"******** Calculations Terminated ********"<<endl;   
return 0; 
} 
